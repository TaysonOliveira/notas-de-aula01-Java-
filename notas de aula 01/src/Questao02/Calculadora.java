package Questao02;

public class Calculadora {

    int n1, n2, total;

    public int somar (){
        return total = n1 + n2;

    }

    public double dividir(){
        return total = n1 / n2;

    }

    public int subtrair() {
        return total = n1 - n2;
    }

    public double multiplicar (){
        return total = n1 * n2;
    }
}